package javabean;

public class Runner {

private int  id;
private String name;
private double salary;
	
private  float cost;

int getId() {
	return id;
}

void setId(int id) {
	this.id = id;
}

String getName() {
	return name;
}

void setName(String name) {
	this.name = name;
}

double getSalary() {
	return salary;
}

void setSalary(double salary) {
	this.salary = salary;
}

float getCost() {
	return cost;
}

void setCost(float cost) {
	this.cost = cost;
}
	


}
	







